package ru.cinema.model.enums;

/**
 * Статусы жизненного цикла контента (фильма/сериала).
 */
public enum ContentStatus {
    /** Черновик — контент создан, но ещё не опубликован */
    DRAFT,
    /** На модерации — контент отправлен на проверку администратору */
    MODERATION,
    /** Опубликован — контент доступен всем пользователям */
    PUBLISHED,
    /** Отклонён — контент не прошёл модерацию */
    REJECTED,
    /** Скрыт — контент временно скрыт (например, по жалобам) */
    HIDDEN,
    /** Удалён — контент помечен как удалённый */
    DELETED
}
