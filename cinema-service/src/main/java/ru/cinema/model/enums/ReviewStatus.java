package ru.cinema.model.enums;

/**
 * Статусы жизненного цикла рецензии.
 */
public enum ReviewStatus {
    /** Черновик — рецензия создана, но не опубликована */
    DRAFT,
    /** Опубликована — рецензия доступна для просмотра */
    PUBLISHED,
    /** На модерации — рецензия отправлена на проверку */
    MODERATION,
    /** Удалена — рецензия помечена как удалённая */
    DELETED
}
